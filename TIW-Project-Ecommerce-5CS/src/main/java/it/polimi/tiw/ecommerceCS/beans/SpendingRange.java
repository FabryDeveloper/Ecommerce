package it.polimi.tiw.ecommerceCS.beans;

public class SpendingRange {
	int min;
	int max;
	double price;
	int idSupp;
	
	public int getMin() {
		return min;
	}
	public void setMin(int min) {
		this.min = min;
	}
	public int getMax() {
		return max;
	}
	public void setMax(int max) {
		this.max = max;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public int getIdSupp() {
		return idSupp;
	}
	public void setIdSupp(int idSupp) {
		this.idSupp = idSupp;
	}
	
	
}
